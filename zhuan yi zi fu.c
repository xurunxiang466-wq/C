#include <stdio.h>
int main()
{
	// \n：换⾏符。
	printf("%c\n", '\'');//⽤于表⽰字符常量'
	printf("%s\n", "\"");//：⽤于表⽰⼀个字符串内部的双引号
	printf("c:\\test\\code\\test.c\n");//⽤于表⽰⼀个反斜杠，防⽌它被解释为⼀个转义序列符
	printf("\a");//警报，这会使得终端发出警报声或出现闪烁，或者两者同时发⽣。
	printf("%c\n", '\130'); //ddd表⽰1~3个⼋进制的数字。 如： \130 表⽰字符X 
    printf("%c\n", '\x30'); // dd表⽰2个⼗六进制数字。 如： \x30 表⽰字符0

	return 0;
}